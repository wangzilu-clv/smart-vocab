export const getWordById = (id: string): Word | undefined => {
  return vocabularyData.find(word => word.id === id);
};

export const getWordsByTheme = (theme: string): Word[] => {
  return vocabularyData.filter(word => word.theme === theme);
};

export const getWordsByCategory = (category: string): Word[] => {
  return vocabularyData.filter(word => word.category === category);
};

export const getWordsByDifficulty = (difficulty: Word['difficulty']): Word[] => {
  return vocabularyData.filter(word => word.difficulty === difficulty);
};

export const getWordsByPrefix = (prefix: string): Word[] => {
  return vocabularyData.filter(word => word.prefix === prefix);
};

export const getAllThemes = (): string[] => {
  const themes = new Set(vocabularyData.map(word => word.theme));
  return Array.from(themes);
};

export const getAllCategories = (): string[] => {
  const categories = new Set(vocabularyData.map(word => word.category));
  return Array.from(categories);
};
